def classify_tag(text):

    text=text.lower()

    ai_keywords=[
        "ai","인공지능","생성형","gpt","llm","머신러닝","딥러닝"
    ]

    fintech_keywords=[
        "핀테크","fintech","결제","payment","가상자산",
        "블록체인","디지털 금융"
    ]

    for k in ai_keywords:
        if k in text:
            return "AI"

    for k in fintech_keywords:
        if k in text:
            return "Fintech"

    return "Digital"


