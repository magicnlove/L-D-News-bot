def decode_google_news_url(url):
    """
    googlenewsdecoder 라이브러리를 사용하여 Google News RSS URL을
    실제 기사 URL로 변환합니다. 기존 수동 base64 디코딩을 대체합니다.
    """
    try:
        result = new_decoderv1(url)
        if result.get("status") and result.get("decoded_url"):
            print(f"DEBUG_DECODE: 성공 → {result['decoded_url']}")
            return result["decoded_url"]
    except Exception as e:
        print(f"ERROR_DECODE: googlenewsdecoder 실패 → {e}")
    return None


