def calculate_importance(news):

    important_keywords=[
        "ai","인공지능","핀테크","fintech",
        "투자","규제","플랫폼","생성형","증권"
    ]

    big_companies=[
        "삼성","네이버","카카오","신한투자","한국투자","하나투자",
        "google","microsoft","amazon",
        "tesla","openai"
    ]

    text=(news["title"]+" "+news.get("summary","")).lower()

    score=0

    for k in important_keywords:
        if k in text:
            score+=2

    for c in big_companies:
        if c.lower() in text:
            score+=3

    return score


