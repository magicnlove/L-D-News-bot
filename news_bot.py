def remove_domain_duplicates(news):

    seen_domains=set()
    filtered=[]

    for n in news:

        domain=urlparse(n["link"]).netloc

        if domain not in seen_domains:

            filtered.append(n)
            seen_domains.add(domain)

    return filtered

