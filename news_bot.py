def remove_duplicates(news):

    seen_urls = set()
    filtered = []

    for n in news:

        # URL 중복 제거
        if n["link"] in seen_urls:
            continue

        # 제목 유사도 제거
        duplicate = False
        for f in filtered:
            if similar(n["title"], f["title"]) > 0.85:
                duplicate = True
                break

        if not duplicate:
            filtered.append(n)
            seen_urls.add(n["link"])

    return filtered

    # Removed remove_domain_duplicates as it was overly aggressive with Google News links
    # If domain-based filtering is desired, it should be applied to the final resolved URLs
    # filtered=remove_domain_duplicates(filtered)

    return filtered

