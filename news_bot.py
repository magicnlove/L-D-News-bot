def get_article(url, session=None):
    article_text = ""
    requester = session if session else requests

    headers = {
        'User-Agent': 'Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/120.0.0.0 Safari/537.36'
    }

    print(f"DEBUG: get_article 시작 → {url}")

    # Step 1: Google News RSS CBM URL이면 googlenewsdecoder로 복호화
    final_article_url = url
    if "news.google.com" in url:
        decoded = decode_google_news_url(url)
        if decoded:
            final_article_url = decoded
            print(f"DEBUG: 복호화 성공 → {final_article_url}")
        else:
            # Step 2: 복호화 실패 시 requests로 직접 follow redirect
            print("DEBUG: 복호화 실패, requests redirect 시도")
            try:
                resp = requester.get(url, allow_redirects=True, timeout=5, headers=headers)
                effective = resp.url
                if "news.google.com" not in effective:
                    final_article_url = effective
                    print(f"DEBUG: redirect 최종 URL → {final_article_url}")
            except Exception as e:
                print(f"ERROR: redirect 실패 → {e}")
                return ""

    # Step 3: Newspaper3k로 본문 추출
    print(f"DEBUG: 최종 기사 URL → {final_article_url}")
    try:
        article = Article(final_article_url)
        article.download()
        article.parse()
        article_text = article.text
        print(f"DEBUG: Newspaper3k 추출 길이 → {len(article_text)}")
    except Exception as e:
        print(f"DEBUG: Newspaper3k 실패 → {e}")
        article_text = ""

    # Step 4: Newspaper3k 실패 시 BeautifulSoup fallback
    if not article_text.strip() or len(article_text.strip()) < 100:
        print(f"DEBUG: BeautifulSoup fallback 시작 → {final_article_url}")
        try:
            fb_resp = requester.get(final_article_url, allow_redirects=True, timeout=5, headers=headers)
            fb_resp.raise_for_status()
            soup = BeautifulSoup(fb_resp.text, 'html.parser')

            for tag in soup(['script', 'style', 'header', 'footer', 'nav', 'aside', 'form', 'iframe', 'noscript']):
                tag.decompose()

            # 국내 주요 언론사 CSS 클래스명 추가 보완
            CONTENT_CLASSES = [
                # 국내 언론사
                'article-body', 'article_body', 'article_content', 'articleCont',
                'article-view-body', 'view_con', 'view_content', 'news_content',
                'news_contents', 'newsct_article', 'dic_area',  # 네이버 뉴스
                'articleBodyContents',  # 조선비즈 등
                'article-text', 'article__body', 'text_area', 'cnt_text',
                'story_content', 'cont_article', 'article_txt',
                # 해외 언론사
                'entry-content', 'post-content', 'main-content', 'story-body',
            ]

            paragraphs = []
            main_area = soup.find('article') or soup.find('main')

            if not main_area:
                for cls in CONTENT_CLASSES:
                    main_area = soup.find(class_=cls)
                    if main_area: break

            target = main_area if main_area else soup
            for p in target.find_all('p'):
                txt = p.get_text(separator=' ', strip=True)
                if len(txt) > 40:
                    paragraphs.append(txt)

            article_text = '\n\n'.join(paragraphs)
            print(f"DEBUG: BeautifulSoup 추출 길이 → {len(article_text)}")

        except Exception as e:
            print(f"ERROR: BeautifulSoup fallback 실패 → {e}")
            article_text = ""

    print(f"DEBUG: 최종 본문 길이 → {len(article_text)}")
    return article_text[:3000]

